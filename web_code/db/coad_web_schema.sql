id_matches AS (
  SELECT
    a.drug_slug,
    a.alias_order,
    md.molregno,
    md.chembl_id,
    md.pref_name,
    md.max_phase,
    md.molecule_type,
    0 AS match_priority
  FROM drug_aliases a
  JOIN chembl.molecule_dictionary md
    ON md.chembl_id = a.preferred_chembl_id
  WHERE a.preferred_chembl_id IS NOT NULL
),
name_matches AS (
  SELECT
    a.drug_slug,
    a.alias_order,
    md.molregno,
    md.chembl_id,
    md.pref_name,
    md.max_phase,
    md.molecule_type,
    1 AS match_priority
  FROM drug_aliases a
  JOIN chembl.molecule_dictionary md
    ON md.pref_name = upper(a.search_name)
  WHERE a.preferred_chembl_id IS NULL
