DROP SCHEMA IF EXISTS coad_web CASCADE;
CREATE SCHEMA coad_web;

CREATE TABLE IF NOT EXISTS coad_web.mutation_gene_frequencies (
  gene_symbol text PRIMARY KEY,
  mutation_records integer NOT NULL,
  mutated_sample_count integer NOT NULL,
  mutated_sample_percent numeric(6, 2) NOT NULL,
  top_variant_classification text,
  source_schema text NOT NULL DEFAULT 'bio_tcga',
  source_table text NOT NULL DEFAULT 'mc3_public_maf',
  display_order integer NOT NULL
);

CREATE TABLE IF NOT EXISTS coad_web.protein_structure_targets (
  gene_symbol text PRIMARY KEY,
  protein_name text NOT NULL,
  uniprot_id text NOT NULL,
  alphafold_url text NOT NULL,
  protein_length integer,
  project_reason text NOT NULL,
  structure_note text NOT NULL,
  source_note text NOT NULL DEFAULT 'Curated public protein metadata for website display'
);

CREATE TABLE IF NOT EXISTS coad_web.mutation_hotspots (
  hotspot_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  gene_symbol text NOT NULL REFERENCES coad_web.mutation_gene_frequencies(gene_symbol) ON DELETE CASCADE,
  protein_change text NOT NULL,
  amino_acid_position integer,
  amino_acids text,
  mutation_label text NOT NULL DEFAULT 'Hotspot',
  sample_count integer NOT NULL,
  mutation_records integer NOT NULL,
  variant_classification text,
  cbioportal_mutation_mapper_url text NOT NULL,
  source_schema text NOT NULL DEFAULT 'bio_tcga',
  source_table text NOT NULL DEFAULT 'mc3_public_maf',
  display_order integer NOT NULL
);

CREATE TABLE IF NOT EXISTS coad_web.coad_drug_sources (
  source_key text PRIMARY KEY,
  source_name text NOT NULL,
  source_url text NOT NULL,
  used_for text NOT NULL,
  access_date date NOT NULL,
  source_note text NOT NULL
);

CREATE TABLE IF NOT EXISTS coad_web.coad_treatment_drugs (
  drug_slug text PRIMARY KEY,
  drug_name text NOT NULL,
  nci_drug_url text NOT NULL,
  nci_cancer_type text NOT NULL DEFAULT 'Colon and rectal cancer',
  evidence_label text NOT NULL DEFAULT 'NCI colorectal cancer drug list',
  source_key text NOT NULL REFERENCES coad_web.coad_drug_sources(source_key),
  display_order integer NOT NULL
);

CREATE TABLE IF NOT EXISTS coad_web.coad_drug_compounds (
  compound_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  drug_slug text NOT NULL REFERENCES coad_web.coad_treatment_drugs(drug_slug) ON DELETE CASCADE,
  drug_name text NOT NULL,
  compound_name text NOT NULL,
  chembl_id text NOT NULL,
  molregno integer,
  molecular_formula text,
  canonical_smiles text,
  standard_inchi_key text,
  max_phase numeric,
  molecule_type text,
  structure_image_url_or_svg text,
  chembl_source_note text NOT NULL DEFAULT 'Curated from local chembl schema',
  source_schema text NOT NULL DEFAULT 'chembl',
  source_table text NOT NULL DEFAULT 'molecule_dictionary, compound_properties, compound_structures',
  UNIQUE (drug_slug, chembl_id)
);

CREATE TABLE IF NOT EXISTS coad_web.coad_drug_indications (
  indication_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  drug_slug text NOT NULL REFERENCES coad_web.coad_treatment_drugs(drug_slug) ON DELETE CASCADE,
  chembl_id text,
  indication_text text,
  mesh_heading text,
  efo_term text,
  source_schema text NOT NULL DEFAULT 'chembl',
  source_table text NOT NULL DEFAULT 'drug_indication'
);

WITH coad_maf AS (
  SELECT
    m.tumor_sample_barcode,
    m.hugo_symbol,
    m.variant_classification
  FROM bio_tcga.mc3_public_maf m
  JOIN bio_tcga.tcga_cdr_tcga_cdr c
    ON c.type = 'COAD'
   AND c.bcr_patient_barcode = substring(m.tumor_sample_barcode from 1 for 12)
  WHERE COALESCE(m.hugo_symbol, '') NOT IN ('', 'Unknown')
),
sample_denominator AS (
  SELECT COUNT(DISTINCT tumor_sample_barcode)::numeric AS sample_count
  FROM coad_maf
),
gene_counts AS (
  SELECT
    hugo_symbol,
    COUNT(*)::int AS mutation_records,
    COUNT(DISTINCT tumor_sample_barcode)::int AS mutated_samples
  FROM coad_maf
  GROUP BY hugo_symbol
),
variant_rank AS (
  SELECT
    hugo_symbol,
    variant_classification,
    ROW_NUMBER() OVER (
      PARTITION BY hugo_symbol
      ORDER BY COUNT(*) DESC, variant_classification
    ) AS variant_rank
  FROM coad_maf
  GROUP BY hugo_symbol, variant_classification
),
ranked_genes AS (
  SELECT
    g.hugo_symbol,
    g.mutation_records,
    g.mutated_samples,
    ROUND(100.0 * g.mutated_samples / NULLIF(d.sample_count, 0), 2) AS sample_frequency_pct,
    v.variant_classification,
    ROW_NUMBER() OVER (ORDER BY g.mutated_samples DESC, g.mutation_records DESC, g.hugo_symbol) AS display_order
  FROM gene_counts g
  CROSS JOIN sample_denominator d
  LEFT JOIN variant_rank v
    ON v.hugo_symbol = g.hugo_symbol
   AND v.variant_rank = 1
)
INSERT INTO coad_web.mutation_gene_frequencies (
  gene_symbol,
  mutation_records,
  mutated_sample_count,
  mutated_sample_percent,
  top_variant_classification,
  display_order
)
SELECT
  hugo_symbol,
  mutation_records,
  mutated_samples,
  sample_frequency_pct,
  variant_classification,
  display_order
FROM ranked_genes
WHERE display_order <= 30
   OR hugo_symbol IN ('APC', 'TP53', 'KRAS', 'BRAF', 'PIK3CA', 'SMAD4')
ORDER BY display_order;

