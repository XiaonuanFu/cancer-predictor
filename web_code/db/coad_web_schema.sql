INSERT INTO coad_web.coad_drug_compounds (
  drug_slug,
  drug_name,
  compound_name,
  chembl_id,
  molregno,
  molecular_formula,
  canonical_smiles,
  standard_inchi_key,
  max_phase,
  molecule_type,
  structure_image_url_or_svg
)
SELECT
  d.drug_slug,
  d.drug_name,
  COALESCE(r.pref_name, d.drug_name),
  r.chembl_id,
  r.molregno,
  cp.full_molformula,
  cs.canonical_smiles,
  cs.standard_inchi_key,
  r.max_phase,
  r.molecule_type,
  CASE
    WHEN r.chembl_id IS NULL THEN NULL
    ELSE 'https://www.ebi.ac.uk/chembl/api/data/image/' || r.chembl_id || '.svg'
  END
FROM ranked_matches r
JOIN coad_web.coad_treatment_drugs d
  ON d.drug_slug = r.drug_slug
LEFT JOIN chembl.compound_properties cp
  ON cp.molregno = r.molregno
LEFT JOIN chembl.compound_structures cs
  ON cs.molregno = r.molregno;

