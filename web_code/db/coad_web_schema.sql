WITH coad_maf AS (
  SELECT
    m.tumor_sample_barcode,
    m.hugo_symbol,
    m.hgvsp_short,
    m.protein_position,
    m.amino_acids,
    m.variant_classification
  FROM bio_tcga.mc3_public_maf m
  JOIN bio_tcga.tcga_cdr_tcga_cdr c
    ON c.type = 'COAD'
   AND c.bcr_patient_barcode = substring(m.tumor_sample_barcode from 1 for 12)
  WHERE COALESCE(m.hugo_symbol, '') NOT IN ('', 'Unknown')
    AND m.hgvsp_short LIKE 'p.%'
    AND m.hgvsp_short <> 'p.?'
),
hotspots AS (
  SELECT
    hugo_symbol,
    hgvsp_short,
    NULLIF(regexp_replace(split_part(COALESCE(protein_position, ''), '/', 1), '[^0-9]', '', 'g'), '')::int AS amino_acid_position,
    amino_acids,
    variant_classification,
    COUNT(DISTINCT tumor_sample_barcode)::int AS mutated_samples,
    COUNT(*)::int AS mutation_records,
    ROW_NUMBER() OVER (
      ORDER BY COUNT(DISTINCT tumor_sample_barcode) DESC, COUNT(*) DESC, hugo_symbol, hgvsp_short
    ) AS display_order
  FROM coad_maf
  GROUP BY hugo_symbol, hgvsp_short, protein_position, amino_acids, variant_classification
  HAVING COUNT(DISTINCT tumor_sample_barcode) >= 3
)
INSERT INTO coad_web.mutation_hotspots (
  gene_symbol,
  protein_change,
  amino_acid_position,
  amino_acids,
  sample_count,
  mutation_records,
  variant_classification,
  cbioportal_mutation_mapper_url,
  display_order
)
SELECT
  h.hugo_symbol,
  h.hgvsp_short,
  h.amino_acid_position,
  h.amino_acids,
  h.mutated_samples,
  h.mutation_records,
  h.variant_classification,
  'https://www.cbioportal.org/mutation_mapper?standaloneMutationMapperGeneTab=' || h.hugo_symbol,
  h.display_order
FROM hotspots h
JOIN coad_web.mutation_gene_frequencies g
  ON g.gene_symbol = h.hugo_symbol
WHERE h.display_order <= 30
ORDER BY h.display_order;

